namespace Aspose.Slides.Web.API.Clients.Enums
{
	/// <summary>
	/// The enum defines how presentation slides should be split.
	/// </summary>
	public enum SplitTypes
	{
		/// <summary>
		/// Splitting slide-by-slide.
		/// </summary>
		SlideBySlide = 0,

		/// <summary>
		/// Splitting to two groups: even and odd slide numbers (starting from 1).
		/// </summary>
		EvenOdd = 1,

		/// <summary>
		/// Splitting by groups with fixed number of slides.
		/// </summary>
		Number = 2,

		/// <summary>
		/// Splitting by slide number ranges.
		/// </summary>
		Range = 3
	}
}
